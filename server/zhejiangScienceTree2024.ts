/**
 * 浙教版初中《科学》（2024 版主线）知识树 — 按分册 › 章 › 节细化为独立知识点节点，
 * 节名与册序参考公开目录与教材常见结构；keywords 用于 OCR/模型文本映射。
 * 可按校本在新数组中追加章节或微调 id（需同步迁移掌握度记录）。
 */

/** 课纲/树数据版本，供客户端缓存与掌握度迁移对齐 */
export const SCIENCE_TREE_VERSION = '2024.1';

export type ScienceTreeNode = {
  id: string;
  label: string;
  keywords?: string[];
  children?: ScienceTreeNode[];
  /** 关联本地资料相对路径（中考专题树叶子） */
  relPath?: string;
};

function K(...words: string[]): string[] {
  return words;
}

function leaf(id: string, label: string, keywords: string[]): ScienceTreeNode {
  return { id, label, keywords };
}

function branch(id: string, label: string, keywords: string[], children: ScienceTreeNode[]): ScienceTreeNode {
  return { id, label, keywords, children };
}

/** 章：若干节，每节若干知识点叶子 */
function chapter(
  bookPrefix: string,
  chNum: number,
  title: string,
  sectionSpecs: { title: string; points: [string, string[]][] }[],
): ScienceTreeNode {
  const chId = `${bookPrefix}.ch${chNum}`;
  return branch(
    chId,
    title,
    K(title.replace(/^第\d+章\s*/, '').slice(0, 8)),
    sectionSpecs.map((sec, si) => {
      const secId = `${chId}.s${si + 1}`;
      const secShort = sec.title.replace(/^\d+\.\d+\s*/, '').slice(0, 12);
      return branch(secId, sec.title, K(secShort), sec.points.map(([plab, kw], pi) => leaf(`${secId}.k${pi + 1}`, plab, kw)));
    }),
  );
}

